
 create table pessoa (
       pes_id int8 generated by default as identity,
        pes_ativo boolean not null,
        pes_cpf varchar(14) not null,
        end_bairro varchar(255),
        end_cidade varchar(255),
        end_complemento varchar(255),
        end_estado varchar(255),
        end_logradouro varchar(255),
        end_numero varchar(255),
        pes_nome varchar(255) not null,
        primary key (pes_id)
    );

