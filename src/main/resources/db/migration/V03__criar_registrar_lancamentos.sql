
 create table public.lancamento (
       lan_id int8 generated by default as identity,
        lan_data_pagamento date,
        lan_data_vencimento date not null,
        lan_descricao varchar(255) not null,
        lan_observacao varchar(255),
        lan_tipo_lancamento varchar(255) not null,
        lan_valor numeric(19, 2) not null check (lan_valor>=0),
        cat_id int8 not null,
        pes_id int8 not null,
        primary key (lan_id)
    );

 alter table if exists public.lancamento 
       add constraint categoria_id 
       foreign key (cat_id) 
       references public.categoria;

    
    alter table if exists public.lancamento 
       add constraint pessoa_id 
       foreign key (pes_id) 
       references pessoa;
       
       
